package lab13.assignment_13_3.src.sample;

public class Convert {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("Chinese -> " + Integer.toHexString((int) '中') + ", " + Integer.toHexString((int) '文'));
		System.out.println("English -> " + Integer.toHexString((int) '英') + ", " + Integer.toHexString((int) '文'));
		System.out.println(
				"title -> " + Integer.toHexString((int) '简') + ", " + Integer.toHexString((int) '单') + " L10n");
		// Chinese=中文
		// English=英文
		// title=简单L10n

		System.out.println("title -> "  + Integer.toHexString((int) '简') + ", " + Integer.toHexString((int) '单') + ", " + Integer.toHexString((int) '表') + ", " + Integer.toHexString((int) '格'));
		
		System.out.println("id -> "  + Integer.toHexString((int) '学') + ", " + Integer.toHexString((int) '号'));
	
		System.out.println("firstname -> "  + Integer.toHexString((int) '名'));
		
		System.out.println("lastname -> "  + Integer.toHexString((int) '姓'));
		
		System.out.println("SSN -> "  + ", " + Integer.toHexString((int) '社') + ", " + Integer.toHexString((int) '保') + ", " + Integer.toHexString((int) '号'));
		
		System.out.println("street -> "  + ", " + Integer.toHexString((int) '街') + ", " + Integer.toHexString((int) '道'));
		
		System.out.println("city -> "  + ", " + Integer.toHexString((int) '城') + ", " + Integer.toHexString((int) '市'));
		
		System.out.println("state -> "  + ", " + Integer.toHexString((int) '状') + ", " + Integer.toHexString((int) '态'));
		
		System.out.println("zip -> "  + Integer.toHexString((int) '邮') + ", " + Integer.toHexString((int) '政') + ", " + Integer.toHexString((int) '编') + ", " + Integer.toHexString((int) '码'));
		
		System.out.println("search -> "  + ", " + Integer.toHexString((int) '搜') + ", " + Integer.toHexString((int) '索'));
		
		System.out.println("save -> "  + ", " + Integer.toHexString((int) '节') + ", " + Integer.toHexString((int) '省'));
		
		System.out.println("clear -> "  + ", " + Integer.toHexString((int) '清') + ", " + Integer.toHexString((int) '除'));
	}
	
	
	
	

}
